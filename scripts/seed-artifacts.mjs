async function publishOne(sample) {
  const body = sample.buffer ?? Buffer.from(sample.content, "utf-8");
  const blob = new Blob([body], { type: sample.mime });
  const file = new File([blob], sample.filename, { type: sample.mime });

  const form = new FormData();
  form.append("file", file);
  form.append("title", sample.title);
  form.append("description", sample.description);
  form.append("tags", sample.tags);

  const res = await fetch(`${API_URL}/api/artifacts`, { method: "POST", body: form });
  const data = await res.json();

  if (!res.ok) {
    throw new Error(data.error ?? `HTTP ${res.status}`);
  }

  return data.artifact;
}

async function main() {
  console.log(`Seeding simulated AI artifacts → ${API_URL}/api/artifacts\n`);

  // Health check
  try {
    const health = await fetch(`${API_URL}/api/artifacts`);
    if (!health.ok) {
      const err = await health.json().catch(() => ({}));
      throw new Error(err.error ?? `API returned ${health.status}`);
    }
  } catch (e) {
    console.error("Cannot reach Artifact Hub API.");
    console.error("Start the app first:  npm run dev");
    console.error(e instanceof Error ? e.message : e);
    process.exit(1);
  }

  const results = [];
  for (const sample of SAMPLES) {
    process.stdout.write(`  • ${sample.title} … `);
    try {
      const artifact = await publishOne(sample);
      console.log(`✓ ${artifact.id.slice(0, 8)}…`);
      results.push(artifact);
    } catch (e) {
      console.log(`✗ ${e instanceof Error ? e.message : e}`);
    }
  }

  console.log(`\nPublished ${results.length}/${SAMPLES.length} artifacts.`);
  console.log(`Gallery: ${API_URL}/`);
  if (results[0]) {
    console.log(`Example: ${API_URL}/artifacts/${results[0].id}`);
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
